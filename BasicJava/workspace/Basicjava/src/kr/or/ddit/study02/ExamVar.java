package kr.or.ddit.study02;

public class ExamVar {
	
	public static int a = 4;
	
	public static void main(String[] args) {
		int x = 3;
		int y = 2;
		int z = x+y;
		System.out.println("x = "+x);
		System.out.println("y = "+y);
		System.out.println("x+y = "+z);
		
		System.out.println("a = "+a);
	}
}
